	#include "alx_ncur.h"

	#include "about.h"
	#include "clui.h"
	#include "data.h"
	#include "init.h"
	#include "tui.h"
	#include "print.h"
	#include "start.h"


int	main	(int argc, char *argv[])
{
	/* Init values */
	alx_start_curses();
	init_values();
	alx_pause_curses();

	/* Parse command line options */
	parser(argc, argv);

	/* Print copyright () and wait for any key to continue */
								print_verbose(1, print_cpright, -1);
								print_verbose(1, getchar, -1);

	/* Start () */
	start_switch();

	/* Menu () */
	if (!flag_exit) {
		alx_resume_curses();
		menu_main();
	}

	/* End curses */
	alx_end_curses();

	return	0;
}

